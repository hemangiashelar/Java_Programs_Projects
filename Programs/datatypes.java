public class datatypes {
    public static void main(String[] args) {
        /*
         * 1)primitive 2)No primitive primitive:-int, char,
         * float,double,longint,shortint, boolean nonprimitive:-String Array classes
         * interface
         */
        float var = 3;
        System.out.println("float=" + var);// 4 bytes
        double var2 = 4.45;
        System.out.println("double=" + var2);// 8 bytes
        char var3 = 'h'; // string:-sequence of characters.
        System.out.println("char=" + var3);
        boolean var4 = true;
        System.out.println("boolean= " + var4);
        short var5 = 3000;
        System.out.println("short=" + var5);
        long var6 = 1000000000;
        System.out.println("long =" + var6);
        String var7 = "Hi guys how are u ?";
        System.out.println("string=" + var7); // non primitive

    }

}
